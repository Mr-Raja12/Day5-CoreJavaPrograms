public class QuotientAndRemainder {
    public static void main(String[] args) {
        int num1=14, num2=2;
        int Quotient = num1/num2;
        int remainder = num1%num2;
        System.out.println("Quotient is: " +Quotient);
        System.out.println("remainder is: " +remainder);
    }
}


